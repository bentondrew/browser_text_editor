<template>
  <div id="textEditor">
    <form id="editor" @submit.self.stop.prevent="onSave">
      <p>
        <label for="fileName">File Name:</label>
        <input id="fileName" v-model="file.name" placeholder="example.txt" />
      </p>
      <p>
        <label for="fileContent">File Content:</label>
        <textarea
          id="fileContent"
          v-model="file.content"
          placeholder="Enter file contents."
        ></textarea>
      </p>
      <div id="fileActions">
        <input type="submit" value="Save" />
        <button @click.self.stop.prevent="clearFile">Clear</button>
      </div>
    </form>
    <div id="localFile">
      <hr />
      <h1>{{ file.name }}</h1>
      <h2>{{ file.content }}</h2>
    </div>
    <div id="savedFile" v-show="showSaved">
      <hr />
      <h1>{{ savedFile(file.name).name }}</h1>
      <h2>{{ savedFile(file.name).content }}</h2>
    </div>
  </div>
</template>

<script>
import { mapGetters, mapMutations } from "vuex";

export default {
  name: "TextEditor",
  data() {
    return {
      file: {
        name: null,
        content: null
      }
    };
  },
  methods: {
    ...mapMutations("files", ["addFile", "updateFileContent"]),
    onSave() {
      if (this.fileExist(this.file.name)) {
        this.updateFileContent(this.file);
      } else {
        this.addFile(this.file);
      }
      this.flash("File saved!", "success", { timeout: 3000 });
    },
    showSaved() {
      if (this.fileExist(this.file.name)) {
        return true;
      } else {
        return false;
      }
    },
    savedFile() {
      var sFile = this.getFile(this.file.name);
      if (sFile) {
        return sFile;
      } else {
        return { name: "", content: "" };
      }
    },
    proceedClear() {
      var proceedClear = true
      if (!this.fileExist(this.file.name)) {
        this.$dialog
        .confirm("File in editor not saved." +
                 " Click Proceed to clear file without saving." +
                 " Otherwise click Cancel and save before clearing file.")
        .then(function(dialog) {
          proceedClear = true
        })
        .catch(function() {
          proceedClear = false
        })
      } else {
        var sFile = this.getFile(this.file.name);
        if (sFile.content !== this.file.content) {
          this.$dialog
          .confirm("File in editor has been changed since last save." +
                   " Click Proceed to clear file without saving." +
                   " Otherwise click Cancel and save before clearing file.")
          .then(function(dialog) {
            proceedClear = true
          })
          .catch(function() {
            proceedClear = false
          })
        }
      }
      return proceedClear
    },
    clearFile() {
      if (this.proceedClear()) {
        this.file.name = null;
        this.file.content = null;
      }
    }
  },
  computed: {
    ...mapGetters("files", ["fileExist", "getFile"])
  }
};
</script>

